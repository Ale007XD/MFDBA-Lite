from dataclasses import dataclass, field
from enum import Enum
from typing import Any, Dict, List, Optional

from mfdballm.models.tool_call import ToolCall


class StepType(str, Enum):
    """
    Canonical step types used by execution trace and tests.

    Using str Enum allows:
    - easy JSON serialization
    - compatibility with existing step_type: str field
    """

    LLM = "llm"
    TOOL = "tool"
    FINAL = "final"


@dataclass
class StepResult:
    """
    Represents a single execution step in the runtime.

    This object is produced by the ExecutionEngine and later stored
    inside ExecutionTrace for navigation and debugging.

    IMPORTANT:
    step_type remains a string for backward compatibility with
    existing runtime code and serialized traces.
    """

    # order of execution
    step_index: int

    # step type (llm/tool/final)
    step_type: str

    # input given to this step
    input: Any = None

    # output produced by this step
    output: Any = None

    # tool calls generated by LLM
    tool_calls: List[ToolCall] = field(default_factory=list)

    # results returned from tools
    tool_results: List[Any] = field(default_factory=list)

    # additional metadata
    metadata: Dict[str, Any] = field(default_factory=dict)

    # error message if step failed
    error: Optional[str] = None

    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> "StepResult":
        """
        Reconstruct StepResult from dictionary representation.
        Used by trace serializer and replay tools.
        """

        # normalize tool calls
        tool_calls = [
            tc if isinstance(tc, ToolCall) else ToolCall.from_dict(tc)
            for tc in data.get("tool_calls", [])
        ]

        return cls(
            step_index=data["step_index"],
            step_type=data["step_type"],
            input=data.get("input"),
            output=data.get("output"),
            tool_calls=tool_calls,
            tool_results=data.get("tool_results", []),
            metadata=data.get("metadata", {}),
            error=data.get("error"),
        )
